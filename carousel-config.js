function openBuildingCarousel(buildingId) {
    const config = buildingCarouselConfig[buildingId];
    if (!config) {
        console.error('Building config not found:', buildingId);
        return;
    }

    const modal = document.getElementById('building-carousel-modal');
    const titleEl = document.getElementById('building-carousel-title');
    const containerEl = document.getElementById('building-carousel-slides');
    const indicatorsEl = document.getElementById('building-carousel-indicators');
    const counterEl = document.getElementById('building-carousel-counter');

    titleEl.textContent = config.emoji + ' ' + config.title;

    containerEl.innerHTML = config.images.map((img, index) => `
        <div class="carousel-slide ${index === 0 ? 'active' : ''}">
            <img src="${img.src}" alt="${img.caption}" onerror="this.src='https://via.placeholder.com/800x500/F4ECE0/8B6B4D?text=图片加载失败'">
            <div class="carousel-caption">${img.caption}</div>
        </div>
    `).join('');

    indicatorsEl.innerHTML = config.images.map((_, index) => `
        <span class="carousel-indicator ${index === 0 ? 'active' : ''}" onclick="goToBuildingSlide('${buildingId}', ${index})"></span>
    `).join('');

    counterEl.innerHTML = `<span id="building-carousel-current">1</span> / ${config.images.length}`;

    modal.dataset.buildingId = buildingId;
    modal.dataset.total = config.images.length;
    modal.dataset.current = 0;

    modal.style.display = 'block';
}

function closeBuildingCarousel() {
    document.getElementById('building-carousel-modal').style.display = 'none';
}

function changeBuildingSlide(direction) {
    const modal = document.getElementById('building-carousel-modal');
    const buildingId = modal.dataset.buildingId;
    const total = parseInt(modal.dataset.total);
    let current = parseInt(modal.dataset.current);

    current += direction;
    if (current >= total) current = 0;
    if (current < 0) current = total - 1;

    showBuildingSlide(buildingId, current);
}

function goToBuildingSlide(buildingId, index) {
    showBuildingSlide(buildingId, index);
}

function showBuildingSlide(buildingId, index) {
    const modal = document.getElementById('building-carousel-modal');
    const slides = modal.querySelectorAll('.carousel-slide');
    const indicators = modal.querySelectorAll('.carousel-indicator');
    const counter = document.getElementById('building-carousel-current');

    slides.forEach((slide, i) => {
        slide.classList.toggle('active', i === index);
    });

    indicators.forEach((ind, i) => {
        ind.classList.toggle('active', i === index);
    });

    counter.textContent = index + 1;
    modal.dataset.current = index;
}
